test("create a ship", () => {
  let gameboard = createGameboard("test");
  const obj = createShips("Carrier", 5, gameboard);
  expect(obj.length).toEqual(5);
  expect(obj.hits).toEqual(0);
  expect(obj.name).toEqual("Carrier");
});
